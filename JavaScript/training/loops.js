/* -------------------------------- FOR LOOP -------------------------------- */

for (let i = 0; i < 10; i++) {
    console.log(i + 1);
}

/* ------------------------------- WHILE LOOP ------------------------------- */

let i = 10;
while (i < 20) {
    console.log(i + 1);
    i++;
}